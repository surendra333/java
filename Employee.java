package MyFirstProject;

public class Employee {
       private String name;
       private int id;
       private long salary;
       public String getname() {
    	   return name;
       }
       public int getid() {
    	   return id;
       }
       public long getsalary()
       {
    	   return salary;
       }
       public void setname(String name) {
    	   this.name=name;
       }
       public void setid(int id) {
    	   this.id=id;
       }
       public void setsalary(long salary) {
    	   this.salary=salary;
       }

}
